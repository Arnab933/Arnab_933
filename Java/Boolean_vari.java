public class Boolean_vari {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        /*
         * Boolean is process to check a condition is true or false .We can create a
         * boolean method that
         * returns a result that may be true or false,similarly we can create boolean
         * variable that also check
         * a codition is true or false
         */
        boolean c = a < b;
        if (c)
            System.out.println("The condition is true");
        else
            System.out.println("The condition is false");
    }
}
